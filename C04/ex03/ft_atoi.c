int	ft_atoi(char *str)
{
	int	s;
	int	res;

	s = 1;
	res = 0;
	while (((*str >= 9 && *str <= 13) || *str == ' ') && *str)
	{
		str++;
	}
	while (*str && (*str == '-' || *str == '+'))
	{
		if (*str == '-')
		{
			s *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str)
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (s * res);
}
