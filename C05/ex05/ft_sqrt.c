int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	if (!(nb % 2))
		i = 2;
	else
		i = 3;
	while (i < nb / i)
		i += 2;
	if (i * i == nb)
		return (i);
	return (0);
}
